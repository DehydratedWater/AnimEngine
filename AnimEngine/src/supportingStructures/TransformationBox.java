package supportingStructures;

public class TransformationBox 
{
	public float X, Y, scaleX, scaleY, WindowScale;

	public TransformationBox(float X, float Y, float scaleX, float scaleY, float WindowScale) 
	{
		this.X = X;
		this.Y = Y;
		this.scaleX = scaleX;
		this.scaleY = scaleY;
		this.WindowScale = WindowScale;
	}
	
}
